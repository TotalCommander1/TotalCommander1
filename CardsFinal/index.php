СОБИРАЙТЕ ВАШИХ СОЮЗНИКОВ
<p>
Во время партии в Magic вы становитесь Planeswalker-ом, в распоряжении которого находится арсенал могучих существ и сокрушительных заклинаний. Но вам не справиться без союзников! За 20 лет истории Magic сюжетная линия игры развивалась в десятках различных миров, в каждом из которых были свои герои, злодеи и конфликты. Игроки наблюдали, как орды Вампиров, Вервольфов и Зомби почти уничтожили человечество в Иннистраде , как ниндзя и самураи сражались с духами в Камигаве , и даже стали свидетелями того, как в Саге Урзы война между братьями чуть не привела к уничтожению всего мироздания. Некоторые из наиболее значимых персонажей, например, Planeswalker-ы Джейс Белерен и Чандра Налаар , появились на картах, которые вы можете положить в свою колоду. Такие карты нелегко достать, но оказавшись на поле битвы, они могут переломить ход сражения в вашу пользу. Для этого и нужны друзья!
