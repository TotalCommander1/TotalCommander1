ПОЛЕЗНЫЕ ИНСТРУМЕНТЫ: АУРЫ
<p>
Итак, вы собрали армию существ. Теперь вам нужно достойное оружие. В Magic вы найдете все необходимое: от карт Аур и Снаряжения, которые усиливают одно из ваших существ, до чар, которые действуют на все поле битвы. Ауры — это особые карты, которые прикрепляются к вашим существам. Обычно прикрепленная к существу Аура увеличивает его размер или дает новые способности. Например, Полет дает существу способность Полета, а Шкура Тролля делает существо больше, и его становится гораздо сложнее убить. Есть и вредоносные Ауры, которые можно прикрепить к существу оппонента, чтобы ослабить его. Аура остается на существе, пока существо не умрет, или что-то не уничтожит саму Ауру.
<p>
ПОЛЕЗНЫЕ ИНСТРУМЕНТЫ: СНАРЯЖЕНИЕ
<p>
Карты Снаряжения похожи на Ауры, но в отличие от них, остаются на поле битвы, даже если снаряженное существо умирает. Кроме того, вы можете перемещать Снаряжение с одного существа на другое — и пусть ваш оппонент гадает, что вы предпримете! Дайте существу Щит Согласителя, и оно сможет храбро броситься в бой, или вооружите его Изрыгателем Огня, чтобы стереть с лица земли любого, кто осмелится встать на его пути. Другой отличный способ поменять правила игры — карты чар. Эти карты ни к чему не прикрепляются, они просто находятся на поле битвы. Например, чары В Глубокой Чаще в каждом ходу могут давать вам дополнительную землю, а Превращение в Сон обращает существа оппонента в зыбкие иллюзии.
<p>
КАК ПРОХОДИТ БОЙ
<p>
АТАКА СУЩЕСТВАМИ
<p>
Собрать кровожадную орду существ на поле битвы — лишь половина задачи. Чтобы выиграть в партии Magic, нужно атаковать! Ваш оппонент начинает игру с 20 жизнями, так что для того, чтобы добиться победы, атаковать придется снова и снова. Вот почему существа так полезны: если не найти способ остановить их, они могут нанести огромное количество повреждений. Обратите внимание, что в бою вы не можете напрямую атаковать существа противника. Вы атакуете оппонента, а он сам решает, как ему блокировать и блокировать ли вообще. Это значит, что перед атакой имеет смысл взглянуть на стол и спросить себя: а как бы вы блокировали на месте оппонента? Поскольку ваш соперник блокировать может как угодно, вы никогда заранее не знаете, чем обернется атака. Впрочем, в этом нет ничего страшного. Запомните главное правило: сомневаешься — атакуй!
<p>
БЛОКИРОВАНИЕ АТАК
<p>
После того как вы решили отправить свои существа в атаку, ваш оппонент выбирает, каким образом он будет блокировать. Иногда всем вашим атакующим удается остаться незаблокированными. Тогда оппонент просто теряет некоторое количество жизней. Однако случается и так, что ваша атака упирается в блок соперника. В этом случае начинаются жестокие схватки между существами! Во время боя у вас будет несколько возможностей воплотить в жизнь свои коварные замыслы и разыграть мгновенные заклинания или активировать способности: перед выбором атакующих, сразу после выбора атакующих и даже после выбора блокирующих. Разыграв мгновенное заклинание, которое увеличивает силу вашего существа или уничтожает во время боя важное для оппонента существо, вы можете полностью разрушить его планы. Но не забывайте: то же самое может проделать и соперник!
