ЭФФЕКТНЫЕ ЗАКЛИНАНИЯ
<p>
Карты существ находятся в центре внимания многих колод Magic, но помимо них есть множество других замечательных карт. Существа могут атаковать, только попав на поле битвы, так что ваш оппонент может выстроить стратегию, не позволяющую им это сделать. Другие заклинания, такие как волшебство и чары, могут оказать мощное воздействие на игру. А мгновенные заклинания позволяют застать соперника врасплох. Если разыграть их в нужный момент, они могут нанести серьезный удар оппоненту. Предположим, например, что вы атакуете небольшим существом, а у вашего оппонента есть существо большого размера, которым он блокирует. В обычной ситуации более крупное существо растоптало бы вашего небольшого атакующего. Но у вас есть выход: мгновенное заклинание, которое называется Исполинский Рост. Как можно догадаться по имени карты, она превращает вашего маленького атакующего в серьезного противника, и теперь блокирующему существу оппонента придется несладко! Стало интересно? Правила для других заклинаний можно прочитать в полном руководстве по игре.
