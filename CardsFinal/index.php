НАНЕСЕНИЕ ПОВРЕЖДЕНИЙ
<p>
Итак, атакующие и блокирующие существа объявлены, и вы с соперником использовали все хитрые трюки, которые были у вас в запасе. Пришло время наносить повреждения! Атакующие существа, оставшиеся незаблокированными, наносят повреждения оппоненту, приближая вас к победе. Если же существо заблокировано, оно наносит повреждения не оппоненту, а заблокировавшему его существу. Блокирующее существо, в свою очередь, наносит повреждения заблокированному. Если существо получает повреждения, равные или превышающие его выносливость, оно уничтожается и отправляется на кладбище. (По сути это просто стопка отыгравших карт, но «кладбище» звучит гораздо эффектнее!)
<p>
ПОСЛЕ БОЯ
<p>
После окончания боя все уцелевшие существа остаются на поле битвы, чтобы сражаться дальше. Это одна из причин, по которой существа так популярны: попав на поле битвы, они остаются серьезной угрозой, пока соперник их не уничтожит. Не волнуйтесь: все время помнить, сколько повреждений получило каждое существо, не придется. В конце каждого хода все они полностью исцеляются. Если у вас есть заклинания или какие-то еще способы нанести повреждения, вы можете прикончить раненых существ до окончания хода.
<p>
ЗАКЛИНАНИЯ И ПРОЦЕСС ИГРЫ
<p>
РАЗЫГРЫВАНИЕ ЗАКЛИНАНИЙ
<p>
Во время игры в Magic вы будете призывать существ, отправлять их в битву, обмениваться заклинаниями и стараться разрушить планы своего оппонента. Время от времени обстановка будет серьезно накаляться, и в этом тоже состоит часть веселья! Вы должны знать, когда вы можете разыграть то или иное заклинание, поэтому давайте познакомимся с этапами хода. Чтобы упорядочить хаос, ход каждого игрока в Magic разделяется на фазы. Фазы всегда расположены в одинаковой последовательности, так что через несколько партий вы к ним привыкнете. Основные свои действия вы проводите в главной фазе. Именно в ней вы разыгрываете земли и заклинания. Эта фаза настолько важна, что в каждом ходу их две. Первая главная фаза проходит до вашей атаки, а вторая — сразу после боя.
<p>
ОСТАЛЬНЫЕ ЭТАПЫ ХОДА, ЧАСТЬ 1
<p>
Вы уже знаете о главной фазе и фазе боя, а теперь давайте разберем оставшиеся этапы хода. Ваш ход всегда начинается с начальной фазы, которая делится на три шага. Прежде всего, во время шага разворота вы разворачиваете (возвращаете в вертикальное положение) все свои повернутые карты. Во время следующего шага, шага поддержки, обычно ничего не происходит, но у некоторых карт есть способности, которые имеют действие именно тогда. Наконец, в начале вашего шага взятия карты вы берете одну карту из своей библиотеки (колоды). Во время шага поддержки и шага взятия карты вы можете разыгрывать мгновенные заклинания и активировать способности. После начальной следует главная фаза. Если вкратце, то в главной фазе вы можете разыграть землю из руки и разыгрывать заклинания.
<p>
ОСТАЛЬНЫЕ ЭТАПЫ ХОДА, ЧАСТЬ 2
<p>
Затем следует фаза боя. Именно в ней вы атакуете оппонента своими существами. Существо не может атаковать в том ходу, когда оно вышло на поле битвы, но если в начале хода оно уже находилось на поле битвы, оно может пойти в атаку! После боя начинается вторая главная фаза. Если в первой главной фазе вы не разыграли землю, то можете сделать это сейчас. Также в этой фазе вы можете разыгрывать заклинания. Обычно имеет смысл разыгрывать заклинания существ именно во второй главной фазе, а не в первой. Так у вашего оппонента будет минимум информации. Если же вы, например, разыграете большое существо до боя, это может повлиять на то, как будет блокировать соперник. А поскольку атаковать существами, которые вышли на поле битвы в этом ходу, вы все равно не можете, то лучше разыгрывать их во второй главной фазе.
